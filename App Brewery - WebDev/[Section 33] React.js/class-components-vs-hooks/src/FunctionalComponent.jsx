import React, { useState } from "react";

const FunctionalComponent = () => {
  const [count, setCount] = useState(0);

  function increase() {
    setCount(count + 1);
  }

  return (
    <div>
      <h1>{count}</h1>
      <button onClick={increase}>+</button>
    </div>
  );
};

export default FunctionalComponent;
